<br/><h1>Søgetips</h1>

<div class="info-box">
<b>Bemærk</b> : For at få fuldt udbytte af allearter.dk's søgefunktion anbefales den 
til enhver tid nyeste version af den browser du benytter. Ældre browser-versioner <i>kan</i>
virke langsomme eller slet ikke virke overhovedet. Som udgangspunkt anbefales at man benytter
browserne <b>Chrome</b>, <b>FireFox</b> eller seneste officielle version af <b>Internet Explorer</b>.
</div>
<div class="info-text">
<br/><h2>Søgesiden på allearter.dk består af fire elementer:</h2>
<ol>
<li><b>Søgefunktion</b> med felter, bokse m.v. til indtastning og afkrydsning af de ønskede søgekriterier (venstre side af skærmen)<br/></li>
<li><b>Listevisning</b>, Oversigt over arter, der opfylder søgekriterierne<br/></li>
<li><b>Detaljevisning</b>. Samtlige informationer i databasen om en enkelt art - ses ved klik på artens videnskabelige navn<br/></li>
<li><b>Print-/downloadfunktion</b><br/></li>
</ol>

<br/><h3>1. Søgefunktion</h3>
<p>
For information om de enkelte søgekriterier - før musen hen over de respektive ugle-ikoner.<br/>
Søgekriterierne kan kombineres på kryds og tværs.<br/><br/>
Et klik på "Nulstil" nulstiller samtlige søgekriterier. Dog bibeholdes valget af danske eller videnskabelige navne.<br/>
De enkelte menuer (Fritekstsøgning, artsgrupper etc.) kan foldes sammen ved klik på navnet.
</p>

<br/><h3>2. Listevisning </h3>
<ul>
<li>I "Vis kolonner" kan der vælges, hvilke kolonner, der ønskes vist.</li>
<li>Rækkefølgen af kolonnerne kan ændres ved at trække og flytte kolonnehovederne.</li>
<li>Bredden af de enkelte kolonner kan ændres ved at markere overgangen mellem to feltnavne.</li>
<li>Alfabetisk sortering af de enkelte kolonner kan aktiveres ved klik på kolonnehovedet<strong>*</strong></li>
<li>I boksen "Filtrér resultater" kan den aktuelle søgning begrænses. Filtrering gælder samtlige kolonner angivet i "Vis kolonner" - ikke blot de viste</li>
</ul>
<p> <strong>*</strong> Hvis en prioriteret sorteringsrækkefølge ønskes, f.eks. orden, familie, dansk navn, klikkes der på kolonnehovederne i omvendt rækkefølge - i dette tilfælde dansk navn, familie, orden.
</p> 

<br/><br/><h3>3. Detaljevisning</h3>
<p>
<small>For info om de enkelte informationer i detaljevisning, læs mere her.</small><br/><br/>
I detaljevisning autogenereres et billede af arten fra EOL (Encyclopedia Of Life). I nogle tilfælde er det dog ikke den pågældende art, der vises, men en repræsentant for slægten. I andre tilfælde vise blot et verdenskort - eller ingenting.<br/><br/>
Hvis "Videnskabelige navne" er valgt i klassifikations-menuen, kan de danske navne (hvis der indgår sådanne på allearter.dk) ses som popup ved rollover på de enkelte klassifikationsniveauer. Og tilsvarende kan de videnskabelige navne ses, hvis danske navne er valgt.
</p> 

<br/><br/><h3>4. Print-/downloadfunktion </h3>
<ul>
<li><b>Print</b> : Her kan fifles med opsætningen i form af margener og liggende eller stående format. Bemærk, at printet afspejler den liste, der vises på skærmen med valgte kolonner samt rækkefølge, sortering og filtrering af disse  - og evt. detaljevisning for én eller flere arter.<br/><br/></li>
<li><b>CSV</b> : Download det aktuelle søgeresultat i csv-format. Som ved print afspejler listen skærmvisningen.<br/><br/></li>
<li><b>XLS</b> : Download det aktuelle søgeresultat i "Excel-venligt" csv-format. Som ved print afspejler listen skærmvisningen.<br/><br/></li>
<li><b>PDF</b> : Her genereres en PDF over søgeresultatet i liggende format<br/><br/></li>
<li><b>CSV - samtlige felter</b> : Download samtlige databaseinformationer tilknyttet det aktuelle søgeresultat. Heriblandt klassifikationskategorier, forvaltningskategorier og notefelter - ca. 60 kolonner i alt.<br/></li>
</ul> 
